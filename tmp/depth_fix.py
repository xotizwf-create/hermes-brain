"""ФИКС глубины: (1) персона — глубина вместо экономии вызовов + запрет угадывать;
(2) «Маршрутная карта» — переформулированная дисциплина (не «мало вызовов», а «не наугад»)."""
import shutil
import subprocess
import sys
import time
from pathlib import Path

sys.path.insert(0, "/var/www/albery")
import app  # noqa: E402,F401
from mcp import context_server as cs  # noqa: E402

CFG = Path("/root/.hermes/config.yaml")
bak = CFG.with_name(f"config.yaml.bak-depthfix-{time.strftime('%Y%m%d_%H%M%S')}")
shutil.copy2(CFG, bak)
print("бэкап конфига:", bak)

OLD = ("Всегда отвечай по-русски, деловым тоном, кратко и по существу, без кавайных смайликов. "
       "Дисциплина работы: в начале работы с инструментами компании вызывай "
       "start_here_always_read_ai_instructions и следуй Маршрутной карте — правильный инструмент "
       "с первого раза, обычно 1-2 вызова, без пробных; факты проверяй инструментами, не выдумывай; "
       "чего не можешь — честно скажи и предложи путь.")
NEW = ("Всегда отвечай по-русски, деловым тоном, по существу, без кавайных смайликов. "
       "ГЛУБИНА ВАЖНЕЕ СКОРОСТИ: думай, а не отвечай рефлекторно. В начале работы с инструментами "
       "компании вызывай start_here_always_read_ai_instructions и следуй Маршрутной карте. "
       "Проверяй СТОЛЬКО источников, сколько нужно для точного ответа (живая оргструктура, "
       "регламенты и база знаний, матрица решений, задачи, переписки) — лишний вызов инструмента "
       "лучше поверхностного ответа; экономить вызовы на содержательном вопросе запрещено. "
       "НИКОГДА НЕ УГАДЫВАЙ: не называй ответственного, срок, цифру или правило, если это не "
       "подтверждено конкретным источником — вместо догадки скажи, что именно проверил, чего в "
       "данных нет и что нужно настроить/уточнить. В ответе показывай, на чём он основан. "
       "Чего не можешь — честно скажи и предложи путь.")

text = CFG.read_text(encoding="utf-8")
assert OLD in text, "якорь персоны не найден"
CFG.write_text(text.replace(OLD, NEW, 1), encoding="utf-8")
import yaml  # noqa: E402
cfg = yaml.safe_load(CFG.read_text(encoding="utf-8"))
assert "ГЛУБИНА ВАЖНЕЕ СКОРОСТИ" in cfg["agent"]["personalities"]["albery"]
print("персона обновлена: глубина > скорость, запрет угадывать")

subprocess.run(["systemctl", "restart", "hermes-gateway"], check=True)
time.sleep(8)
st = subprocess.run(["systemctl", "is-active", "hermes-gateway"], capture_output=True, text=True)
print("hermes-gateway:", st.stdout.strip())
assert st.stdout.strip() == "active", f"гейтвей упал — восстанови {bak}"

# --- Маршрутная карта: дисциплина «не наугад», а не «мало вызовов» ---
cur = cs.tool_get_ai_instructions({"path": "Маршрутная карта"})
content = (cur.get("folders") or [{}])[0].get("content") if isinstance(cur, dict) else None
if not content:
    content = cur.get("content") if isinstance(cur, dict) else ""
assert "Дисциплина скорости" in (content or ""), "не прочитал текущую Маршрутную карту"

OLD_BLOCK = """Дисциплина скорости (для ОБЫЧНЫХ запросов):
1. На простой запрос — максимум 1–2 вызова инструментов. Ответил — остановись.
2. Не делай пробных вызовов без обязательных полей — получишь отказ и потеряешь целый ход.
3. Двух неудачных попыток поиска достаточно: честно скажи, что нашёл и каких данных не хватает.
4. Если нужного инструмента нет в твоём наборе — это не твоя роль: скажи прямо и направь к Основному агенту (по доступам — Александр Никитенко)."""

NEW_BLOCK = """Дисциплина работы (глубина важнее скорости):
1. Не вызывай инструменты наугад и не выгружай данные «на всякий случай» — но и НЕ экономь вызовы на содержательном вопросе. Сколько источников нужно для точного ответа — столько и проверь.
2. Вопрос про людей, ответственность, правила, сроки, цифры = проверь ВСЕ релевантные источники: живую оргструктуру (get_bitrix_departments / get_org_structure), регламенты и базу знаний (search_company_knowledge), при необходимости задачи и переписки. Одного источника мало.
3. НИКОГДА не угадывай. Не называй имя ответственного, срок, цифру или правило, если это не подтверждено конкретным источником. Нет подтверждения — так и скажи: что проверил, чего в данных нет, что нужно настроить или уточнить. Уверенный ответ без источника — это обман.
4. В ответе показывай, на чём он основан (по оргструктуре… / по регламенту «…» …).
5. Не делай пробных вызовов без обязательных полей — получишь отказ и потеряешь целый ход.
6. Если нужного инструмента нет в твоём наборе — это не твоя роль: скажи прямо и направь к Основному агенту (по доступам — Александр Никитенко)."""

assert OLD_BLOCK in content, "блок дисциплины скорости не найден дословно"
new_content = content.replace(OLD_BLOCK, NEW_BLOCK, 1)
r = cs.tool_upsert_ai_instruction({"path": "Маршрутная карта", "content": new_content})
folder = (r or {}).get("folder") or {}
body = folder.get("content") or ""
print("Маршрутная карта обновлена | длина:", len(body))
print("  «1–2 вызова» осталось:", "1–2 вызова" in body or "1-2 вызова" in body)
print("  есть запрет угадывать:", "НИКОГДА не угадывай" in body)
print("\nОТКАТ конфига при необходимости: cp", bak, CFG)
