# --- read DATABASE_URL ---
url = None
for line in open('/var/www/albery/.env', encoding='utf-8'):
    if line.startswith('DATABASE_URL='):
        url = line.split('=', 1)[1].strip()
        break
assert url, "no DATABASE_URL"

import psycopg
conn = psycopg.connect(url)
cur = conn.cursor()

cur.execute("select prompt_text from ai_prompts where id=%s", ('b941c658-5656-45f8-b7ab-81bdc8e47b26',))
old_prompt = cur.fetchone()[0]
cur.execute("select content from ai_instruction_folders where id=%s", ('dd471db7-70a9-4224-922b-cb1b27756196',))
old_instr = cur.fetchone()[0]

d = json.load(open(JOBS, encoding='utf-8'))
jobs = d if isinstance(d, list) else d.get('jobs')
job = next(j for j in jobs if j.get('id') == '4baf5fcd80d1')
old_cron = job['prompt']

# validate ALL replacements before writing anything
new_prompt = apply(old_prompt, PROMPT_REPLS, 'ai_prompts')
new_instr = apply(old_instr, INSTR_REPLS, 'instruction')
new_cron = apply(old_cron, CRON_REPLS, 'cron')

# all good -> backups
open(f"{SECURE}/owner_weekly_prompt.bak.friday.{TS}", 'w', encoding='utf-8').write(old_prompt)
open(f"{SECURE}/weekly_instr.bak.friday.{TS}.md", 'w', encoding='utf-8').write(old_instr)
shutil.copy2(JOBS, f"{JOBS}.bak.friday.{TS}")

# DB commit
cur.execute("update ai_prompts set prompt_text=%s where id=%s", (new_prompt, 'b941c658-5656-45f8-b7ab-81bdc8e47b26'))
cur.execute("update ai_instruction_folders set content=%s, updated_at=now() where id=%s", (new_instr, 'dd471db7-70a9-4224-922b-cb1b27756196'))
conn.commit()

# jobs.json write (in place, preserve all fields)
job['prompt'] = new_cron
with open(JOBS, 'w', encoding='utf-8') as fh:
    json.dump(d, fh, ensure_ascii=False, indent=2)

print("OK applied", TS)
print("ai_prompts   len", len(old_prompt), "->", len(new_prompt))
