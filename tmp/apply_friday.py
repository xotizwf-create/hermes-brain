def apply(text, repls, label):
    for old, new in repls:
        c = text.count(old)
        if c != 1:
            raise SystemExit(f"[ABORT {label}] expected 1 occurrence, found {c} for: {old[:70]!r}")
        text = text.replace(old, new)
    return text

