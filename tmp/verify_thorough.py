"""Проверка фикса на РЕАЛЬНОМ сценарии провала: в истории уже есть НЕПОЛНЫЙ ответ агента
(нашёл 2 должности), человек просит то же самое. Должен перепроверить и найти всех."""
import subprocess
import time

ENV = {"HOME": "/root", "PATH": "/usr/local/bin:/usr/bin:/bin"}
EXPECTED = ["Палей", "Андрусяк", "Школьникова", "Степанян", "Горюнова", "Строгонов", "Хапова", "Тагирова"]

# воспроизводим инъекцию истории, как это делает бот
PROMPT = (
    "История этого диалога (предыдущие реплики, помни их):\n"
    "Пользователь: Можешь помочь настроить оргструктуру в битриксе?\n"
    "Ассистент: Да. Сейчас в портале один отдел «Битрикс», все 17 сотрудников в нём, "
    "у большинства должности не заполнены.\n"
    "Пользователь: Почитай документацию нашей компании и уточни, у кого какая должность.\n"
    "Ассистент: Нашёл подтверждённые должности: Евгений Палей — Генеральный директор, "
    "Анастасия Андрусяк — Бухгалтер. Нужно уточнить вручную: Александр Никитенко, "
    "Артур Степанян, Наталья Горюнова, Дмитрий Строгонов, Оксана Хапова, Олеся Тагирова, "
    "Юлия Школьникова, Софья Погорелова и другие.\n\n"
    "Текущее сообщение пользователя:\n"
    "У кого какая должность? Собери полную картину по всем сотрудникам."
)

for i in range(2):
    t0 = time.time()
    r = subprocess.run(["hermes", "-z", PROMPT, "-t", "agent-main,web", "--yolo"],
                       capture_output=True, text=True, timeout=600, cwd="/root", env=ENV)
    out = (r.stdout or "").strip()
    found = [n for n in EXPECTED if n in out]
    print(f"\n[прогон {i+1}] {time.time()-t0:.0f}с | должностей найдено: {len(found)}/8 {found}")
    print("  ответ:", out[:500].replace("\n", " / "))
